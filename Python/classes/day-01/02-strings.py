
#Strings

print ("hola"+" "+"lucas") #se puede concatenar datos strings

print("Me llamo \"Lucas\"") #las barras invertidas (ATL GR + ?) antes de un texto sirve para avisar que lo que sigue no es una cadena sino un caracter o función especial y se tiene que tomar como es

print("Esta es una línea\nY esta es otra línea") #la "n" marca el salto de línea dentro del texto

print("\tEsta es la tercera línea") #la "t" quiere decir "tabular", agrega un espacio de 4 caracteres antes del siguiente

print("This isn\'t a number") #en este caso, al tipearse el apóstrofe de una palabra en inglés, py no lo toma, por lo que se necesita la barra invertida

print('Este signo \\ es una barra invertida') #incluso para tipear barras invertidas como texto se necesitan que tenga antes otra barra invertida para tomarla como es

print("Línea 1\nLínea 2\nLínea 3") #ejercicio string

print("A\tB\tC") #ejercicio string 2
print("D\tE\tF")
print("G\tH\tI")

print("Barra Normal: /\nBarra Invertida: \\") #ejercicio string 3